public class Rectangle {

    public static void main(String[] args) {

        double base = 5.6;
        double altura = 8.5;
        double area = base * altura;
        double perimetro = (2 * base) + (2 * altura);

        System.out.println("Area = " + area);
        System.out.println("Perimetro = " + perimetro);
    }
}
